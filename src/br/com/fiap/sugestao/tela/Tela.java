    public static void geraGrafo()
    {
        g = new CustomSimpleWeightedGraph(MyWeightEdge.class); 

        //Lendo Visitacoes
        VisitacaoInOut inout = new VisitacaoInOut(caminho);

        ArrayList<Visitacao> visitacoes	= inout.LerVisitacoes();

        //Buscando Vertices
        Vertice vertice = new Vertice();
        HashSet<Vertice> vertices = vertice.getVertices(visitacoes);

        //Buscando Arestas
        Aresta aresta = new Aresta();
        ArrayList<Aresta> arestas = aresta.getArestas(visitacoes);

        //Preenchendo os Vertices
        for(Vertice vert : vertices)
        {
            g.addVertex(vert.getVertice());
        }

        //Preenchendo as Arestas
        for(Aresta ar : arestas)
        {
            DefaultWeightedEdge edge = (DefaultWeightedEdge)g.addEdge(ar.getDe(),ar.getPara());
            g.setEdgeWeight(edge,ar.getPeso());
        }

        adapter = new JGraphModelAdapter(g);

        graph = new JGraph(adapter);

        graph.isDragEnabled();
        
        graph.setAutoResizeGraph(true);

        // Control-drag should clone selection
        graph.setCloneable(true);

        // Enable edit without final RETURN keystroke
        graph.setInvokesStopCellEditing(true);

        // When over a cell, jump to its default port (we only have one, anyway)
        graph.setJumpToDefaultPort(true);
    }

