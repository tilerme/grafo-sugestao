    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        boolean ok = true;
        
        if((!this.jRadioButton3.isSelected() && !this.jRadioButton4.isSelected()) || this.jTextField5.getText().isEmpty() || this.jTextField6.getText().isEmpty())
        { 
            JOptionPane.showMessageDialog(this, "Os campos devem ser preenchidos.");
            ok = false;
        }
        try
        {
            Integer aux = Integer.parseInt(this.jTextField5.getText());
        }catch(Exception ex1)
        {
            ok = false;
            JOptionPane.showMessageDialog(this, "Os campos devem ser preenchidos com números inteiros!!");
        }       
        
        if(ok)
        {
            Produto produto = new Produto();
            produto.setCodigo(Integer.parseInt(this.jTextField6.getText()));
            String sexo = "";
            if(this.jRadioButton3.isSelected())
                sexo = "M";
            else
                sexo = "F";
            Visitacao visitacao = new Visitacao(produto,Integer.parseInt(this.jTextField5.getText()),sexo);
            VisitacaoInOut inout = new VisitacaoInOut(caminho);
            inout.IncluirVisitacao(visitacao);

            geraGrafo();

            this.jScrollPane1.setViewportView(graph);

            this.invalidate();
            this.repaint();
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        boolean ok = true;
        
        if((!this.jRadioButton1.isSelected() && !this.jRadioButton2.isSelected()) || this.jTextField1.getText().isEmpty())
        { 
            JOptionPane.showMessageDialog(this, "Os campos devem ser preenchidos.");
            ok = false;
        }
        try
        {
            Integer aux = Integer.parseInt(this.jTextField1.getText());
        }catch(Exception ex1)
        {
            ok = false;
            JOptionPane.showMessageDialog(this, "Os campos devem ser preenchidos com números inteiros!!");
        }       
        
        if(ok)
        {
            ArrayList<Aresta> arestas = new ArrayList<Aresta>();
            String sexo = "";
            if(this.jRadioButton1.isSelected())
                sexo = "M";
            else
                sexo = "F";

            Perfil perfil = new Perfil(Integer.parseInt(this.jTextField1.getText()),sexo);
            if(g.containsVertex(perfil.getPerfil()));
            {
                Set<DefaultWeightedEdge> set = g.edgesOf(perfil.getPerfil());

                for(DefaultWeightedEdge edge : set)
                {
                    int rounded = (int) Math.round(g.getEdgeWeight(edge));
                    arestas.add(new Aresta(String.valueOf(g.getEdgeSource(edge)),String.valueOf(g.getEdgeTarget(edge)),rounded));
                }
                // Em ordem crescente do início do mandato  
                Collections.sort (arestas, new Comparator() {  
                    @Override
                    public int compare(Object o1, Object o2) {  
                        Aresta a1 = (Aresta) o1;  
                        Aresta a2 = (Aresta) o2;  
                        return a1.getPeso() < a2.getPeso() ? +1 : (a1.getPeso() > a2.getPeso() ? -1 : 0);  
                    }  
                });

                this.jTextField2.setText("");
                this.jTextField3.setText("");
                this.jTextField4.setText("");
                
                int contador = 1;
                for(Aresta aresta : arestas)
                {
                    if(contador < 4)
                    {        
                        if(contador == 1)
                            this.jTextField2.setText(aresta.getPara().substring(1));
                        else if(contador == 2)
                            this.jTextField3.setText(aresta.getPara().substring(1));
                        else if(contador == 3)
                            this.jTextField4.setText(aresta.getPara().substring(1));
                        contador++;
                    }else
                         break;
                }
            }            
        }
    }//GEN-LAST:event_jButton1MouseClicked

